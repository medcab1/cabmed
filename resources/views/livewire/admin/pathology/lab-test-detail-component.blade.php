                        MedCab Alcohol Impact Test package measures tests such as Lipid Profile, LFT (Liver Function Test), Gamma-Glutamyl transferase (GGT), Vitamin B12, KFT (Kidney Function Test), Glycosylated Hemoglobin (HbA1C), Vitamin D, Complete Urine Examination (CUE), CBC (complete blood count) test with 73 parameters.



                        What does the Lipid test measure?

                        A lipid profile test helps check the amount of total cholesterol which is the fat in your body. It is essential for understanding your heart health especially if you have conditions like obesity, diabetes or high blood pressure. This test shows if you have healthy cholesterol levels or if you need to make changes to protect your heart. It is a way to keep track of how well your heart is doing and if you need to take any extra steps to stay healthy.



                        What does the LFT test measure?

                        LFT (Liver Function Test) helps detect if there's any swelling, infection, or diseases in your liver. It also checks for any harm caused by liver issues. The results of this test give doctors important information about your liver's well-being and can help them figure out if there is anything wrong that needs attention.



                        What does the Gamma-Glutamyl transferase (GGT) test measure?

                        A gamma-glutamyl transferase (GGT) test checks how much GGT is in your blood. GGT is an enzyme found all over your body but mostly in the liver. If your liver gets hurt then GGT might go into your blood. Having lots of GGT in your blood could mean your liver or bile ducts are not okay.



                        What does the Vitamin B12 test measure?

                        The vitamin B12 blood test measures the level of vitamin B12 in your blood and your red blood cells (RBC) are larger than normal. This test helps find out if you have lower hemoglobin levels. Bigger red blood cells or low hemoglobin can mean a lack of enough vitamin B12 which is important for your body. This test gives a clue about how well your body is using vitamin B12 and if you might need more of it for good health.


                        What does the KFT test measure?

                        The KFT (Kidney Function Test) checks how well your kidneys are working, detects kidney issues and checks if treatment is working. It also checks glomerular filtration rate levels. This test helps in checking how your kidneys are doing and if they are staying healthy. If your kidneys are not working right, it is important to know so you can get the right treatment. The KFT helps your doctor understand how well your kidneys are filtering waste from your blood. It is a key test in keeping your kidneys healthy and catching problems early.



                        What does the HbA1C (Glycosylated Haemoglobin) test measure?

                        A hemoglobin A1C (HbA1C) test is a blood test that measures the amount of blood sugar (glucose) attached to your hemoglobin. This test gives an average of your blood sugar during that time. It helps understand if your sugar levels are in a good range. The results are important for managing conditions like diabetes.



                        What does the Vitamin D test measure?

                        A vitamin D test detects the amount of vitamin D in your blood to ensure your body functions properly. A vitamin D blood test helps your doctor see if you might get weak bones or other bone issues. Patients with serious vitamin D deficiency may feel symptoms like bone pain, arthralgias, myalgias, fatigue, muscle twitching (fasciculations) and weakness.



                        What does the Complete Urine Examination (CUE) test measure?

                        A Complete Urine Examination (CUE) test helps doctors figure out if someone showing signs of Urinary Tract Infection (UTI), kidney issues, metabolic disorders or diabetes actually has these problems. It is a diagnostic test that checks the urine to see what might be causing the symptoms. Doctors use it to find out if there is something wrong with the urinary system or if diabetes or other health issues might be present. It is an important test to understand what is happening inside the body and how to treat it.



